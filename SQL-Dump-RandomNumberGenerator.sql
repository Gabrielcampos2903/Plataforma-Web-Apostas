DROP TABLE ACCOUNTS;
DROP SEQUENCE SEQ_ACCOUNTS;
DROP SEQUENCE SEQ_BETS;
DROP TABLE EVENTS;
DROP TABLE BETS;
DROP TABLE EVENTSDENIED;

CREATE TABLE ACCOUNTS(
ID INTEGER NOT NULL PRIMARY KEY,
COMPLETE_NAME VARCHAR2(100) NOT NULL,
EMAIL VARCHAR2(100) NOT NULL UNIQUE,
PASSWORD VARCHAR2(100) NOT NULL, 
BIRTHDATE VARCHAR2(100) NOT NULL,
TOKEN VARCHAR2(32) NOT NULL,
CONTA VARCHAR2(30) NOT NULL,
WALLET_VALUE NUMBER,
CREATED_AT DATE DEFAULT SYSDATE
);

CREATE SEQUENCE SEQ_ACCOUNTS START WITH 1 INCREMENT BY 1;

CREATE TABLE EVENTS(
TITLE VARCHAR2(50) NOT NULL PRIMARY KEY,
TOKEN VARCHAR2(32) NOT NULL,
DESCRICAO VARCHAR2(150) NOT NULL,
VALORDABET NUMBER NOT NULL,
EVENTDATE VARCHAR2(100) NOT NULL,
DATERECEVEINGBEG VARCHAR2(100) NOT NULL,
DATERECEVEINGEND VARCHAR2(100) NOT NULL,
STATUS VARCHAR2(100) NOT NULL,
COMPLETION VARCHAR2(100) NOT NULL,
CATEGORIA VARCHAR2(100) NOT NULL,
CREATED_AT DATE DEFAULT SYSDATE
);

CREATE SEQUENCE SEQ_BETS START WITH 1 INCREMENT BY 1;
CREATE TABLE BETS(
ID NUMBER PRIMARY KEY,
TOKEN VARCHAR2(32) NOT NULL,
BETVALUE NUMBER NOT NULL,
EVENT_TITLE VARCHAR2(100) NOT NULL,
GUESS VARCHAR2(100) NOT NULL
);



CREATE TABLE EVENTSDENIED(
TITLE VARCHAR2(50) PRIMARY KEY,
EMAIL_CREATING VARCHAR2(100) NOT NULL,
SUMMARY VARCHAR2(150)NOT NULL,
CAUSE VARCHAR2(100) NOT NULL,
STATUS VARCHAR2(100) NOT NULL
);

